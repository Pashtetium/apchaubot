export const getEmoji = (size) => {
    switch (size) {
        case 3-5: return '😬' 
        case 6-8: return '🙄' 
        case 9-11: return '😔' 
        case 12-13: return '😊' 
        case 14-15: return '😎' 
        case 16-18: return '😇' 
        case 19-25: return '🧔‍♂️😲🧔‍♂️' 
        case 25-30: return '☣😨☣' 
    }
}